# 2. Digits

n = int(input())
if n < 10:
    print(1)
elif n < 100:
    print(2)
elif n < 1000:
    print(3)
elif n < 10000:
    print(4)
elif n < 100000:
    print(5)
elif n < 1000000:
    print(6)
elif n < 10000000:
    print(7)
elif n < 100000000:
    print(8)
elif n < 1000000000:
    print(9)
else:
    print(10)
