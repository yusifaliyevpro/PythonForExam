# Ilk həddi b olan və silsilə fərqi a olan ədədi silsilənin n-ci həddinin hesablanması üçün proqram yazın.
# Daxil etdiyimiz a və b ədədləri sıfırdan fərqlidir

b = int(input("Ilk həddi: "))
a = int(input("Silsilə Fərqi: "))
